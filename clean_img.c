#include "fdf.h"

void	clean_img(t_win *win)
{
	int i;

	i = 0;
	while (i <= HEIGHT * WIDTH * 4)
	{
		win->ptr[i] = 0;
		win->ptr[++i] = 0;
		win->ptr[++i] = 0;
		i += 2;
	}
}
